void FirmwareUpdate(String urlRamFirmware,String UrlVersion,String FirmwareVer)
{
  WiFiClientSecure client;
  client.setTrustAnchors(&cert);
  if (!client.connect(host, httpsPort))
  {
    Serial.println("Connection failed");
    return;
  }
  client.print(String("GET ") + UrlVersion + " HTTP/1.1\r\n" +
               "Host: " + host + "\r\n" +
               "User-Agent: BuildFailureDetectorESP8266\r\n" +
               "Connection: close\r\n\r\n");
  while (client.connected())
  {
    String line = client.readStringUntil('\n');
    if (line == "\r")
    {
      //Serial.println("Headers received");
      break;
    }
  }
  String payload = client.readStringUntil('\n');

  Serial.print("File version hien tai la : ");
  Serial.println(FirmwareVer);

  payload.trim();
  Serial.print("File version tren Github la : ");
  Serial.println(payload);
  if (payload.equals(FirmwareVer))
  {
    Serial.println("Device already on latest firmware version");
    ESP.reset();
  }
  else
  {
    ESPhttpUpdate.setLedPin(LED_BUILTIN, LOW);
    t_httpUpdate_return ret = ESPhttpUpdate.update(client, urlRamFirmware);

    switch (ret)
    {
    case HTTP_UPDATE_FAILED:
      Serial.printf("HTTP_UPDATE_FAILD Error (%d): %s\n", ESPhttpUpdate.getLastError(), ESPhttpUpdate.getLastErrorString().c_str());
      break;

    case HTTP_UPDATE_NO_UPDATES:
      Serial.println("HTTP_UPDATE_NO_UPDATES");
      break;

    case HTTP_UPDATE_OK:
      Serial.println("HTTP_UPDATE_OK");
      break;
    }
  }
}

